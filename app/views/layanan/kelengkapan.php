
        <h1 class="mb-4">Surat Tanda Terdaftar untuk Lembaga Kesejahteraan Sosial (LKS)</h1>
        <p><b> <h2 style="text-align:left"> Kelengkapan yang perlu dilampirkan pada waktu Pendaftaran : </h2></b> </p>
        <p>1. Anggaran dasar dan anggaran rumah tangga LKS/Orsos;</p>
        <p>2. Akteu notaris yang telah disyahkan oleh Kementerian Hukum dan HAM;</p>
        <p>3. Surat Keterangan Domisili Sekretariat LKS/Orsos dari kelurahan/desa setempat minimal selama 3 (tiga) tahun;</p>
        <p>4. NPWP milik LKS/Orsos;</p>
        <p>5. Program dan kegiatan pelayanan di bidang penyelenggaraan kesejahteraan sosial;</p>
        <p>6. Rekomendasi dari instansi/Dinas Sosial Kabupaten bagi LKS /Orsos yang mengajukan ke tingkat provinsi;</p>
        <p>7. Rekomendasi dari LKKS Kabupaten;</p>
        <p>8. Struktur Organisasi LKS/Orsos dan susunan kepengurusan LKS/Orsos beserta fotocopy KTP Pengurus (Ketua, Sekretaris dan Bendahara)</p>
        <p>9. Daftar penerima manfaat ;</p>
        <p>10. Foto Sekretariat dan Kegiatan penyelenggaraan kesejahteraan sosial yang dilaksanakan.</p>
