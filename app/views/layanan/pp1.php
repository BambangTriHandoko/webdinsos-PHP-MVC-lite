<h2 class="mb-4">Bantuan Orang Terlantar Dalam Perjalanan(BOTP)</h2>
        <p><b> <h1 style="text-align:left"> Sistem, Mekanisme dan Prosedur Pelayanan : </h1></b> </p>
        <p>1. Orang Terlantar datang ke Dinas Sosial dengan membawa Surat keterangan dari kepolisian;</p>
        <p>2. Petugas Dinas Sosial melakukan registrasi dan verifikasi terhadap kelengkapan persyaratan/dokumen;</p>
        <p>3. Petugas mewawancarai tentang kronologis kerjadian termasuk menanyakan daerah yang dituju;</p>
        <p>4. Petugas memberikan bantuan berupa biaya/uang transport dan uang makan untuk diperjalanan.</p>


