
        <h1 class="mb-4">Fasilitasi Anak jalanan dan Anak Terlantar</h1>
        <p><b> <h2 style="text-align:left"> Sistem, Mekanisme dan Prosedur pelayanan : </h2></b> </p>
        <p>1. Pelapor menyerahkan Anak Terlantar/Anak Jalanan kepada pekerja sosial;</p>
        <p>2. Pekerja sosial menerima laporan dan melakukan register kasus;</p>
        <p>3. Pekerja sosial melakukan asesmen terhadap klien;</p>
        <p>4. Pekerja sosial membuat laporan sosial dan melakukan rujukan (jika diperlukan) dengan diketahui oleh Dinas Sosial;</p>
        <p>5. Pekerja sosial melaporkan hasil penanganan kasus kepada Kasi Perlindungan dan Jaminan Sosial.</p>

   