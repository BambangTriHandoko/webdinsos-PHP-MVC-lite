
        <h1 class="mb-4">Prosedur Permohonan Rekomendasi bagi Lansia Terlantar ke Balai Perlindungan Sosial Tresna Werdha (BPSTW)</h1>
        <p><b> <h2 style="text-align:left"> Sistem, Mekanisme dan Prosedur pelayanan : </h2></b> </p>
        <p><h4>1. Petugas Kelurahan membuat surat permohonan kepada Dinas Sosial tentang permintaan rehabilitasi;</h4></p>
        <p><h4>2. Mencatat dalam buku register;</h4></p>
        <p><h4>3. Petugas Dinas Sosial menugaskan Pekerja Sosial Pendamping Lanjut Usia untuk melakukan verifikasi lapangan;</h4></p>
        <p><h4>4. Pendamping Lanjut Usia menyiapkan persyaratan administratif meliputi : identifikasi dan registrasi, penelusuran dan penelaahan masalah (assessment);</h4></p>
        <p><h4>5. Membuat Rekomendasi;</h4></p>
        <p><h4>6. Mengirimkan ke panti yang dituju.</h4></p>
